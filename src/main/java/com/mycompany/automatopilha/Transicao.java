package com.mycompany.automatopilha;

public class Transicao {
    
    public String de;
    public String para;
    public char letraFita;
    public String leitura;
    public String escrita;
    
    public Transicao(String de, String para, char letraFita, String leitura, String escrita) {
        this.de = de;
        this.para = para;
        this.letraFita = letraFita;
        this.leitura = leitura;
        this.escrita = escrita;
    }
    
}
